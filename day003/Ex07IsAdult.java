
// Напишите программу, которая принимает возраст человека и выводит на консоль,
// является ли он совершеннолетним (18 лет и старше).
import java.util.Scanner;

public class Ex07IsAdult {
    public static void main(String[] args) {
        // Создаем объект Scanner для считывания ввода пользователя
        Scanner scanner = new Scanner(System.in);

        // Запрашиваем у пользователя ввод возраста
        System.out.print("Введите ваш возраст: ");
        // Считываем введенное целое число
        int age = scanner.nextInt();

        // Проверяем корректность введенного возраста
        if (age <= 0) {
            // Если возраст меньше или равен 0, выводим сообщение об ошибке
            System.out.println("Данные некорректны");
        } else {
            // Если возраст корректный, проверяем совершеннолетие
            if (age >= 18) {
                // Если возраст больше или равен 18, выводим сообщение о совершеннолетии
                System.out.println("Вы совершеннолетний");
            } else {
                // Если возраст меньше 18, выводим сообщение о несовершеннолетии
                System.out.println("Вы несовершеннолетний");
            }
        }

        // Закрываем Scanner для освобождения ресурсов
        scanner.close();
    }
}
