public class Ex04Conditions {
    public static void main(String[] args) {
        // if-else

        int x = 12;
        int y = 23;

        if (x > y) {
            System.out.println("Число х больше");
            System.out.println("Число х больше");
        } else {
            System.out.println("Число y больше");
            System.out.println("Число y больше");
        }
    }
}
